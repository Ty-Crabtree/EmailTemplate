"""
Python has 4 default collection types
List
Tuple
Dictionary
Set
"""

a_list = [1, '2', True]  # a list can hold any type of values

a_tuple = (
    "apple", "banana", "cherry")  # a tuple can hold any type of value but thy can not be changed but no duplicates

a_dictionary = {'Fruit': 'apple'}  # a dictionary hold values in a key value pare

a_set = {'first', 'second'}  # similar to the tuple but are also unordered and can have duplicate values
